// Declaração do pacote ao qual a classe For_teste2 pertence
package PrimeiroExercicios;

// Declaração da classe For_teste2
public class For_teste2 {

    // Método principal, ponto de entrada do programa
    public static void main(String[] args) {

        // Estrutura de repetição for que inicializa i como 0, continua enquanto i for menor que 5, incrementa i a cada iteração
        for (int i = 0; i < 5; i++) {
            // Imprime o valor atual de i no console
            System.out.println("Valor de i: " + i);
        }
    }
}